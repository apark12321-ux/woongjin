SOL = {"g1": G1, "g2": G2, "jj": JJ}

def section_of(fname):
    if "족집게" in fname: return "jj"
    if any(p in fname for p in ("_80p_","_81p_","_82p_","그래프_83p")): return "g2"
    return "g1"   # 56~59p 기출

NEG = ["옳지 않은", "틀린", "어색한", "아닌", "잘못"]
def underline_neg(line):
    if line.startswith("@@") or line.strip() in ("(정답)", "(해설)"):
        return line
    for w in NEG:
        line = line.replace(w, "__" + w + "__")
    return line

def transform_body(body):
    """〈보기〉/[보기] 블록 → @@BOGI@@ … @@/BOGI@@, 보기 항목 2칸 띄어쓰기, 부정어 밑줄."""
    out = []; i = 0
    while i < len(body):
        ln = body[i]
        if ln.strip() in ("〈보기〉", "[보기]"):
            items = []; i += 1
            while i < len(body) and body[i] != "":
                it = re.sub(r'^([ㄱ-ㅎ]\.) ', r'\1  ', body[i])
                items.append(it); i += 1
            out.append("@@BOGI@@"); out.extend(items); out.append("@@/BOGI@@")
        else:
            out.append(underline_neg(ln)); i += 1
    return out

