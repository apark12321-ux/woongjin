_HDR_MAP = [
    ("y=ax²+bx+c",  "«y=ax^2 +bx+c»"),
    ("y=a(x-p)²+q", "«y=a(x-p)^2 +q»"),
    ("y=a(x-p)²",   "«y=a(x-p)^2 »"),
    ("y=ax²+q",     "«y=ax^2 +q»"),
    ("y=-ax²",      "«y=-ax^2 »"),
    ("y=ax²",       "«y=ax^2 »"),
]
def _conv_header(s):
    for a, b in _HDR_MAP:
        s = s.replace(a, b)
    return s

