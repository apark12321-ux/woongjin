# ===== 빌드 실행 =====
def run_build():
    made = []
    for fname, imgs, probs in PAGES:
        E._EQ_COUNTER[0] = 0
        out = os.path.join(OUT, fname)
        res = build_document(probs, out, title=None, font=FONT,
                             answers_vertical=True, left_align=True, bracket_number=False)
        if imgs:
            insert_images(out, imgs)
            xml = zipfile.ZipFile(out).read("Contents/section0.xml").decode("utf-8")
            leftover = xml.count("@IMG")
        else:
            leftover = 0
        made.append((fname, res["problems"], res["equations"], leftover, zipfile.is_zipfile(out)))
    print(f"총 {len(made)}개 파일 / 총 문항 {sum(m[1] for m in made)}")
    for m in made:
        print(f"  {m[0]} | 문항 {m[1]} | 수식 {m[2]} | @IMG잔존 {m[3]} | 유효 {m[4]}")

if __name__ == "__main__":
    run_build()
